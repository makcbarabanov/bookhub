-- Очистка ложных «героев» из book_characters
--
-- Причина (GIGO): не import_1445.py, а «↻ Из текста» → service_analyzer.py (regex).
-- Парсер брал слова с заглавной буквы после точки: Мне, На, Не…
-- Фикс парсера: service_analyzer.py (деплой 17:15+).
--
-- 1) Выполнить этот скрипт в DBeaver (bookhub_prod).
-- 2) В UI: 👥 Главные герои → «↻ Из текста».

DELETE FROM book_characters
WHERE name IN (
    'Мне', 'На', 'Не', 'Ну', 'Почему',
    'Потом', 'Разговор', 'Сколько', 'Хотя', 'Каждый', 'Просто',
    'Бег', 'По', 'Прячет', 'Конверт',
    'Идей', 'Именованые', 'Необычная', 'Очередь', 'Право',
    'Реанимация', 'Собрание', 'Дорога', 'Бунт', 'Плоды',
    'Макс Барабанов', 'Макс', 'Артура'
);

UPDATE book_characters
SET name = 'Лучик'
WHERE lower(name) = 'лучик' AND name <> 'Лучик';
